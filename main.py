# –≠—Ç–∏ —Ñ—É–Ω–∫—Ü–∏–∏ —Ç–µ–ø–µ—Ä—å –ø—Ä–æ—Å—Ç–æ –≤–æ–∑–≤—Ä–∞—â–∞—é—Ç –∑–∞—Ä–∞–Ω–µ–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã–µ —Å–ø–∏—Å–∫–∏, –∏–∑–±–µ–≥–∞—è list comprehensions.
def get_main_actions():
    return ["üÜï –ù–∞—á–∞—Ç—å –Ω–æ–≤—ã–π –ø–æ—Å—Ç", "‚öôÔ∏è –ö–æ—Ä—Ä–µ–∫—Ç–∏—Ä–æ–≤–∞—Ç—å –ø—Ä–µ–¥—ã–¥—É—â–∏–π"]

def get_theme_actions():
    return ["–ë–∏–∑–Ω–µ—Å", "–¢–µ—Ö–Ω–æ–ª–æ–≥–∏–∏", "–ü—É—Ç–µ—à–µ—Å—Ç–≤–∏—è", "–ó–¥–æ—Ä–æ–≤—å–µ", "–õ–∏—á–Ω—ã–π –±—Ä–µ–Ω–¥", "–î—Ä—É–≥–∞—è —Ç–µ–º–∞"]

def get_genre_actions():
    return ["–ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏–æ–Ω–Ω—ã–π (–æ–±—É—á–µ–Ω–∏–µ)", "–ü—Ä–æ–¥–∞—é—â–∏–π (AIDA)", "–†–∞–∑–≤–ª–µ–∫–∞—Ç–µ–ª—å–Ω—ã–π (–ª–∞–π—Ñ—Ö–∞–∫)", "–°—Ç–æ—Ä–∏—Ç–µ–ª–ª–∏–Ω–≥ (–ª–∏—á–Ω–∞—è –∏—Å—Ç–æ—Ä–∏—è)", "–ü—Ä–æ–≤–æ–∫–∞—Ü–∏—è (—Ö–∞–π–ø)"]


def main() -> None:
    """–û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –∑–∞–ø—É—Å–∫–∞ –±–æ—Ç–∞."""
    
    logger.info("Starting bot application...")
    
    application = Application.builder().token(TELEGRAM_BOT_TOKEN).build()

    conv_handler = ConversationHandler(
        entry_points=[CommandHandler("start", start)],
        states={
            CHOOSING_ACTION: [
                # ‚úÖ –ò–°–ü–†–ê–í–õ–ï–ù–û: –Ø–≤–Ω—ã–π —Å–ø–∏—Å–æ–∫ –¥–ª—è –æ—Å–Ω–æ–≤–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π
                MessageHandler(filters.Text(get_main_actions()), choose_action)
            ],
            GETTING_ACCESS_CODE: [
                MessageHandler(filters.TEXT & ~filters.COMMAND, handle_access_code)
            ],
            CHOOSING_THEME: [
                # ‚úÖ –ò–°–ü–†–ê–í–õ–ï–ù–û: –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –≤—Å–µ –∫–Ω–æ–ø–∫–∏ –¢–ï–ú–´ –∏ –∫–Ω–æ–ø–∫—É –ù–ê–ó–ê–î
                MessageHandler(filters.Text(get_theme_actions() + ["‚¨ÖÔ∏è –ù–∞–∑–∞–¥"]), choose_theme)
            ],
            CHOOSING_GENRE: [
                # ‚úÖ –ò–°–ü–†–ê–í–õ–ï–ù–û: –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –≤—Å–µ –∫–Ω–æ–ø–∫–∏ –ñ–ê–ù–†–ê –∏ –∫–Ω–æ–ø–∫—É –ù–ê–ó–ê–î
                MessageHandler(filters.Text(get_genre_actions() + ["‚¨ÖÔ∏è –ù–∞–∑–∞–¥"]), choose_genre)
            ],
            GETTING_TOPIC: [
                MessageHandler(filters.TEXT & ~filters.COMMAND, generate_post)
            ],
            GETTING_CORRECTION: [
                 MessageHandler(filters.TEXT & ~filters.COMMAND, correct_post)
            ],
        },
        # –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–Ω–æ–ø–∫–∏ "–û—Ç–º–µ–Ω–∞" –≤—Å–µ–≥–¥–∞ —Å—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç
        fallbacks=[MessageHandler(filters.Text(["‚ùå –û—Ç–º–µ–Ω–∞"]), cancel)],
        allow_reentry=True
    )

    application.add_handler(conv_handler)
    
    logger.info("ü§ñ –ë–æ—Ç –∑–∞–ø—É—â–µ–Ω –∏ –≥–æ—Ç–æ–≤ –∫ —Ä–∞–±–æ—Ç–µ...")
    
    application.run_polling(allowed_updates=Update.ALL_TYPES)

if __name__ == '__main__':
    main()
